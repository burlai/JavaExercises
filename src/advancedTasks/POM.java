	public int getMaxProfit(int n, int[][] profit) {
		long max = 0;
		for (int i = 0; i < n; i++) {
			for (int j = 0; j < n; j++) {
				for (int k = 0; k < masks.length; k++) {
					long value = getProfit(i, j, masks[k], profit, n);
					if (value > max) max = value;
				}
			}
		}
		return (int) max;
	}

	private long getProfit(int x, int y, long mask, int[][] profit, int N) {
		long sum = 0;
		long bit = 1;
		for (int i = 0; i < 8; i++) {
			for (int j = 0; j < 8; j++) {
				if ((mask & bit) != 0) {
					if (x + i >= N || y + j >= N) return 0;
					sum += profit[x + i][y + j];
				}
				bit <<= 1;
			}
		}
		return sum;
	}

